    private void b_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_simpanMouseClicked
        // TODO add your handling code here:
        String sql="Insert into vendor values(?,?,?,?,?,?,?,?)";
        try{
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1,"0");
            stat.setString(2,txt_nama.getText());
            stat.setString(3,txt_bidang.getText());
            stat.setString(4,txt_cp.getText());
            stat.setString(5,txt_alamat.getText());
            stat.setString(6,txt_phone.getText());
            stat.setString(7,txt_email.getText());
            stat.setString(8,txt_keterangan.getText());        
            stat.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Data Vendor Berhasil Disimpan");
            kosong();
            txt_nama.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan : "+ e);
        }
    }//GEN-LAST:event_b_simpanMouseClicked

    private void b_editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_editMouseClicked
        // TODO add your handling code here:
        String b=txt_kode.getText();
        try{
            String sql="update vendor set nama=?, bidang=?, contact=?, alamat=?, phone=?, email=?, keterangan=? where kode='"+b+"'";
            PreparedStatement stat=conn.prepareStatement(sql); 
            stat.setString(1,txt_nama.getText());
            stat.setString(2,txt_bidang.getText());
            stat.setString(3,txt_cp.getText());
            stat.setString(4,txt_alamat.getText());
            stat.setString(5,txt_phone.getText());
            stat.setString(6,txt_email.getText());
            stat.setString(7,txt_keterangan.getText());
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Vendor berhasil diubah");
            kosong();
            this.setVisible(false);
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data gagal disimpan : "+ e);
        }
    }//GEN-LAST:event_b_editMouseClicked

