    public void getDataid(String id){
        //home pp = new home();
        b_simpan.setVisible(false);
        b_edit.setVisible(true);
        jLabel16.setText("                  Ubah Data Vendor");        
        txt_kode.setText(home.id_vendor);
        String a = txt_kode.getText();
        txt_kode.setEnabled(false);
        try{
            String sql="select * from vendor where kode='"+a+"'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql); 
            
            while (rs.next()){
             txt_kode.setText(rs.getString("kode"));
             txt_nama.setText(rs.getString("nama"));
             txt_bidang.setText(rs.getString("bidang"));
             txt_cp.setText(rs.getString("contact"));
             txt_alamat.setText(rs.getString("alamat"));
             txt_phone.setText(rs.getString("phone"));
             txt_email.setText(rs.getString("email"));
             txt_keterangan.setText(rs.getString("keterangan"));
                      }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data vendor gagal dipanggil : "+ e);
            }
    }
    
