    private void b_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_simpanMouseClicked
        // TODO add your handling code here:
        String sql="Insert into barang values(?,?,?,?,?)";
        String asql="select kode from kategori where nama='"+cb_kategori.getSelectedItem().toString()+"'";
        String bsql="select kode from merk where nama='"+cb_merk.getSelectedItem().toString()+"'";
        try{            
            String a="";
            Statement st = conn.createStatement(); ResultSet rs = st.executeQuery(asql); 
            while (rs.next()){a=rs.getString("kode");}
            
            String b="";
            Statement bst = conn.createStatement(); ResultSet brs = bst.executeQuery(bsql); 
            while (brs.next()){b=brs.getString("kode");}

            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1,"0");
            stat.setString(2,txt_nama.getText());
            stat.setString(3,b);
            stat.setString(4,a);
            stat.setString(5,txt_keterangan.getText());                     
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Tipe Barang Berhasil Disimpan");
            kosong();
            txt_nama.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"Data Gagal Disimpan : "+ e);
        }
    }//GEN-LAST:event_b_simpanMouseClicked

