    public void getDataid(String id){
        b_simpan.setVisible(false);
        b_edit.setVisible(true);
        jLabel16.setText("               Ubah Data Tipe Barang");        
        txt_kode.setText(home.id_tipe);
        String a = txt_kode.getText();
        txt_kode.setEnabled(false);
        try{            
            String sql="SELECT barang.kode, barang.nama, kategori.nama, merk.nama, barang.keterangan "
                    + "FROM barang inner join kategori on barang.kode_kategori=kategori.kode "
                    + "join merk on barang.kode_merk=merk.kode "
                    + "where barang.kode='"+a+"'";
           
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql);    
            
            while (rs.next()){
             txt_kode.setText(rs.getString("barang.kode"));
             cb_kategori.setSelectedItem(rs.getString("kategori.nama"));
             cb_merk.addItem(rs.getString("merk.nama")); cb_merk.setSelectedItem(rs.getString("merk.nama"));
             txt_nama.setText(rs.getString("barang.nama"));           
             txt_keterangan.setText(rs.getString("barang.keterangan"));
             }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data Tipe Barang gagal dipanggil : "+ e);
            }
    }
    
