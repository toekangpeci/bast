    private void b_simpanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_simpanMouseClicked
        // TODO add your handling code here:
        String sql="Insert into lokasi values(?,?,?,?)";
        try{
            PreparedStatement stat=conn.prepareStatement(sql);
            stat.setString(1,"0");
            stat.setString(2,txt_nama.getText());
            stat.setString(3,txt_detail.getText());
            stat.setString(4,txt_keterangan.getText());                     
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Lokasi Berhasil Disimpan");
            kosong();
            txt_nama.requestFocus();
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"Data Lokasi Gagal Disimpan : "+ e);
        }
    }//GEN-LAST:event_b_simpanMouseClicked

    private void b_editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_editMouseClicked
        // TODO add your handling code here:
        String b=txt_kode.getText();
        try{
            String sql="update lokasi set nama=?, detail=?,keterangan=? where kode='"+b+"'";
            PreparedStatement stat=conn.prepareStatement(sql); 
            stat.setString(1,txt_nama.getText());
            stat.setString(2,txt_detail.getText());
            stat.setString(3,txt_keterangan.getText());            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data Lokasi berhasil diubah");
            kosong();
            this.setVisible(false);
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data gagal disimpan : "+ e);
        }
    }//GEN-LAST:event_b_editMouseClicked

    private void jPanel15MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel15MouseClicked
        // TODO add your handling code here:
        String a = txt_kode.getText();
        String b = txt_nama.getText();
        int ok = JOptionPane.showConfirmDialog(null,"Hapus Data "+b+"?","konfirmasi Hapus Data",JOptionPane.YES_NO_OPTION);
        if (ok==0){
            String sql="delete from lokasi where kode='"+a+"'";
            try{
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.executeUpdate();
                JOptionPane.showMessageDialog(null,"data lokasi berhasil dihapus");
                kosong();
                this.setVisible(false);
            }
            catch (SQLException e){
                JOptionPane.showMessageDialog (null,"data gagal dihapus"+e);
            }
        }
    }//GEN-LAST:event_jPanel15MouseClicked

