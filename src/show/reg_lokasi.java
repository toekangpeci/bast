    public void getDataid(String id){
        b_simpan.setVisible(false);
        b_edit.setVisible(true);
        jLabel16.setText("          Ubah Data Lokasi");        
        txt_kode.setText(home.id_lokasi);
        String a = txt_kode.getText();
        txt_kode.setEnabled(false);
        try{
            String sql="select * from lokasi where kode='"+a+"'";
            Statement st = conn.createStatement();
            ResultSet rs = st.executeQuery(sql); 
            
            while (rs.next()){
             txt_kode.setText(rs.getString("kode"));
             txt_nama.setText(rs.getString("nama"));
             txt_detail.setText(rs.getString("detail"));
             txt_keterangan.setText(rs.getString("keterangan"));
                      }
        }
        catch (SQLException e){
            JOptionPane.showMessageDialog(null,"data lokasi gagal dipanggil : "+ e);
            }
    }
    
